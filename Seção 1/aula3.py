"""
DocString
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('Luiz Otávio')
print(1, 'Luiz "Otávio"') # iniciando o argumento com aspas simples podemos fazer as duplas aparecerem no print

# Aspas duplas
print("Luiz Otávio")
print(2, "Luiz 'Otávio'") # iniciando o argumento com aspas duplas podemos fazer as simples aparecerem no print

# Escape
print("Luiz \"Otávio\"")

# r
print(r"Luiz \"Otávio\"")